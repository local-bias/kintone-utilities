export type Language = keyof typeof ui;
export type TranslationKey = keyof (typeof ui)[typeof defaultLang];

export const defaultLang = 'ja' satisfies Language;

const isSupportedLang = (lang: string): lang is Language => lang in ui;

/**
 * 指定された言語に対応する翻訳関数を返します。
 * @param lang - 言語コード（例: 'ja', 'en'）。省略時またはサポート外の場合は 'ja' にフォールバック
 */
export function useTranslations(lang?: string) {
  const validLang = lang && isSupportedLang(lang) ? lang : defaultLang;

  return function t(key: TranslationKey, ...args: string[]): string {
    // @ts-ignore サポート外の言語の場合はデフォルト言語の翻訳を使用
    let translation: string = ui[validLang][key] ?? ui[defaultLang][key];

    args.forEach((arg, index) => {
      translation = translation.replace(`{${index}}`, arg);
    });

    return translation;
  };
}
